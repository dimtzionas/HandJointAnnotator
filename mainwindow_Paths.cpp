void MainWindow::update_BASE_Paths()
{
        PATH_BASE_Base = ui->myTextEdit_PATH->toPlainText();
    if (PATH_BASE_Base.endsWith("/") == false)
        PATH_BASE_Base += "/";

    PATH_BASE_PCL      = QString( PATH_BASE_Base + "pcl"         + "/" );
    PATH_BASE_RGB      = QString( PATH_BASE_Base + "rgb"         + "/" );
    PATH_BASE_RGBD     = QString( PATH_BASE_Base + "rgbd"        + "/" );
    PATH_BASE_Depth    = QString( PATH_BASE_Base + "depth"       + "/" );
    PATH_BASE_DepthViz = QString( PATH_BASE_Base + "depth_viz"   + "/" );
    PATH_BASE_Anno     = QString( PATH_BASE_Base + "groundTruth" + "/" );

    QDir().mkdir( PATH_BASE_Anno );

    QStringList myStringListFromPath;
    QRegExp koftis("/");
    myStringListFromPath = PATH_BASE_Base.split(koftis, QString::SkipEmptyParts);
    int szz = myStringListFromPath.size();
    PATH_BASE_SeqCam = QString( QString("/") + myStringListFromPath[szz-2]+ "/" + myStringListFromPath[szz-1] + "/" );
                                        std::cout << PATH_BASE_Base.toStdString() << std::endl;
                                        std::cout << PATH_BASE_SeqCam.toStdString() << std::endl;
    for (int iii=0; iii<szz; iii++)     std::cout << myStringListFromPath[iii].toStdString() << std::endl;
}


